def normalize_lang(lang):
    return lang if lang in SUPPORTED_LANGS else "ru"


def get_lang(request):
    raw = request.GET.get("lang") or request.COOKIES.get(LANG_COOKIE) or getattr(settings, "LANGUAGE_CODE", "ru")
    return normalize_lang(str(raw).split("-")[0].lower())


