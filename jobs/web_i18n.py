def employer_i18n(request):
    lang = get_lang(request)
    wallet_total = None
    if getattr(request.user, "is_authenticated", False):
        # Import locally so this lightweight template context stays usable at startup.
        from .economy import get_or_create_wallet

        wallet_total = get_or_create_wallet(request.user).total_credits
    return {
        "employer_t": t(lang),
        "employer_wallet_total": wallet_total,
        "employer_languages": [
            {
                "code": code,
                "label": LANGUAGE_NAMES.get(code, code.upper()),
                "active": code == lang,
            }
            for code in SUPPORTED_LANGS
        ],
    }


def apply_language_cookie(response, lang):
    response.set_cookie(
        LANG_COOKIE,
        normalize_lang(lang),
        max_age=60 * 60 * 24 * 365,
        samesite="Lax",
    )
    return response


def tr(request, key, default=None):
    return t(get_lang(request)).get(key, default if default is not None else key)
